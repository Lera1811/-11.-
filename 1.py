print(x1,x2)

# 2 –ù–∞–π—Ç–∏ –∏–Ω—Ç–µ—Ä–≤–∞–ª—ã, –Ω–∞ –∫–æ—Ç–æ—Ä—ã—Ö —Ñ—É–Ω–∫—Ü–∏—è –≤–æ–∑—Ä–∞—Å—Ç–∞–µ—Ç

